/// Datos de radiación para un momento concreto, W/m²
#[derive(Debug, Clone, Default, Serialize, Deserialize)]
pub struct RadData {
    /// Mes del año [1, 12]
    pub month: u32,
    /// Día del mes [1, 31]
    pub day: u32,
    /// Hola de reloj para la localización, h [1.0, 24.0]
    pub hour: f32,
    /// Azimuth solar (grados) [-180.0,180.0] (-E, S=0, +W)
    pub azimuth: f32,
    /// Altitud solar (grados) [0.0, 90.0]
    pub altitude: f32,
    /// Radiación directa, W/m²
    pub dir: f32,
    /// Radiación difusa, W/m²
    pub dif: f32,
}

/// Convierte filas en formato .met a RadData pero separadas por comas,
/// Selecciona las columnas correspondientes
/// 7, 1, 6, 14.5,  1.7,  53,  50, 0.00523, 51, 5.2,  70,-112.2, 80.5
macro_rules! to_raddata {
    ($month:literal, $day:literal, $hour:literal, $db_temp:literal, $sky_temp:literal, $rdirhor:literal, $rdifhor:literal, $abs_hulidity:literal, $rel_humidity:literal, $wind_speed:literal, $wind_dir:literal, $azimuth:literal, $zenith:literal) => {
        RadData {
            month: $month,
            day: $day,
            hour: $hour as f32,
            azimuth: -($azimuth),
            altitude: 90.0 - $zenith,
            dir: $rdirhor as f32,
            dif: $rdifhor as f32,
        }
    };
}

